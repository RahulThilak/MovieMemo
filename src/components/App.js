function Search({ query, setQuery }) {
  const inputEl = useRef(null);

  useKey("Enter", function () {
    // Check if the element is already focused and if it is dont do query reset on hitting "Enter" key.
    if (document.activeElement === inputEl.current) return;
    inputEl.current.focus();
    setQuery("");
  });

  return (
    <input
      className="search"
      type="text"
      placeholder="Search movies..."
      value={query}
      onChange={(e) => setQuery(e.target.value)}
      // Ref is connected to this element in a declarative way
      ref={inputEl}
    />
  );
}

